<div class="Fontt" style="padding-left:15px;">
    <br>
    <p style="text-indent:50px;">
        การปรับปรุงพฤติกรรมของเด็กนั้นไม่จำเป็นจะต้องใช้วิธีการใหม่ ๆ เสมอไป บางครั้งการเปลี่ยนแปลงเพียงเล็กน้อยเพียงไม่กี่นาทีต่อวันก็สามารถทำให้เกิดความเปลี่ยนแปลงอย่างยิ่งใหญ่ได้
        เคล็ดลับเล็ก ๆ 5 ข้อต่อไปนี้ หากใช้ร่วมกับวิธีส่งเสริมระเบียบวินัยเชิงบวกอื่น ๆ จะสามารถช่วยลดปัญหาทางพฤติกรรมของเด็กได้อย่างรวดเร็ว
    </p>
    <b>  1. เล่นกับลูกทุกวัน </b>
    <br>
    <p style="text-indent:50px;">
คนส่วนใหญ่มักไม่คิดว่าการเล่นเป็นเครื่องมือในการฝึกระเบียบวินัย แต่ในความเป็นจริงแล้ว การเล่นกับลูกของคุณอาจเป็นวิธีการที่ดีที่สุดวิธีหนึ่งในการป้องกันการเกิดปัญหาทางพฤติกรรม เพราะการให้ความสนใจในเชิงบวกจะช่วยลดพฤติกรรมเรียกร้องจำนวนมากได้
ควรให้ความสนใจอย่างเต็มที่กับลูกของคุณวันละ 10-15 นาที โดยเล่นกับพวกเขาในทุกๆ เกมหรือของเล่นที่พวกเขาเลือก หลีกเลี่ยงการถามคำถามที่มากเกินไปในช่วงเวลานี้ คุณไม่ได้ต้องการให้ช่วงเวลากลายเป็นเวลาทำงานของเด็ก และไม่ต้องคิดมากเกี่ยวกับการที่จะต้องทำให้การเล่นนี้เป็นการเรียนรู้ควบคู่ไปด้วยกัน ขอแค่เล่นให้สนุกก็พอ
    </p>

    <b> 2. ตอบตกลงบ่อย ๆ </b>
    <br>
    <p style="text-indent:50px;">
เมื่อเด็กได้รับคำปฏิเสธตลอดเวลาก็จะเริ่มหงุดหงิดเกี่ยวกับสิ่งที่พวกเขาไม่ได้รับอนุญาตให้ทำ ดังนั้น ก่อนที่คุณจะพูดว่า “ไม่ ลูกไม่สามารถใช้นิ้วระบายสีได้” หรือ “ไม่ เราออกไปข้างนอกไม่ได้นะ” ลองถามตัวเองก่อนว่าทำไมคุณถึงจะปฏิเสธ และให้มั่นใจว่าคุณไม่ได้ตอบปฏิเสธจนกลายเป็นนิสัย
การตอบตกลงในหลาย ๆ เรื่องจะช่วยให้ลูกของคุณคิดถึงสิ่งที่พวกเขาสามารถทำได้ และเมื่อเด็กได้รับอิสระในการค้นหาโอกาสและเรียนรู้เรื่องราวใหม่ๆ พวกเขาก็จะลดความต้องการออกไปนอกกรอบจนเกิดปัญหาตามมาได้
    </p>
<b> 3. ให้ลูกของคุณตื่นตัวอยู่เสมอ</b>
<br>
<p style="text-indent:50px;">
เด็กที่เบื่อจะเริ่มหาวิธีที่จะทำให้ตัวเองสนุกและส่วนใหญ่ก็มักจะเป็นการก่อกวนพี่น้องหรือบทสนทนาของผู้ใหญ่ การทำให้ลูกของคุณตื่นตัวทั้งทางกายและทางจิตอยู่เสมอจะช่วยลดพฤติกรรมที่ไม่เหมาะสมได้

ควรจัดช่วงเวลาในแต่ละวันสำหรับให้ลูกของคุณได้ตื่นตัวอยู่เสมอ
พวกเขาอาจสามารถจัดการกับความสนใจและพฤติกรรมของตนเองได้ดีขึ้นหากได้รับโอกาสให้วิ่งเล่น นอกนั้นกิจกรรมต่างๆ เช่นงานประดิษฐ์ การไปปีนเขา หรือการเล่นกีฬาต่างๆ ก็เป็นวิธีการที่ดีในการกระตุ้นลูกของคุณ
</p>
<b>
4. ฝึกพฤติกรรมที่ดี</b>
<br>
<p style="text-indent:50px;">
ควรมองหาช่วงเวลาสั้น ๆ ในแต่ละวันสำหรับการสอนและช่วยให้ลูกของคุณฝึกฝนพฤติกรรมที่ดี โดยเลือกใช้การสอนพฤติกรรมที่เหมาะสมแทนการส่งไป time-out หรืองดผลประโยชน์ที่พวกเขาเคยได้รับเป็นการทำโทษ

ลองสอนว่า “แทนที่จะหยิบหนังสือออกจากมือของแม่ ลูกควรพูดอย่างสุภาพว่า “ลูกขอหนังสือเล่มนั้นได้ไหมคะ?” แทน หลังจากนั้นก็ให้เวลาลูกของคุณได้ฝึกพฤติกรรมที่เหมาะสมนั้น การเล่นบทบาทสมมติก็เป็นวิธีที่ดีในการให้พวกเขาฝึกฝนทักษะใหม่ ๆ
</p>
<b>
5. พูดเรื่องดี ๆ เกี่ยวกับลูกของคุณให้คนอื่นฟัง </b>
<p style="text-indent:50px;">
แม้ว่าการชมลูกของคุณต่อหน้าจะเป็นเรื่องสำคัญ แต่การที่พวกเขาได้ยินคุณพูดถึงเรื่องดี ๆ ของพวกเขาให้คนอื่นฟังก็เป็นการแรงจูงใจที่ดีให้กับพวกเขา เช่น ขณะที่พูดคุยกับคุณย่าหรือคุณยายทางโทรศัพท์ คุณอาจจะพูดว่า “หลังจากที่ลูกกลับมาจากโรงเรียน เขาก็เลือกที่จะนั่งลงและทำการบ้านของตัวเองทันที ฉันรู้สึกภูมิใจมากที่ลูกเลือกทำในสิ่งที่ดี”

เด็ก ๆ รู้ว่าคนที่พวกเขารักชื่นชอบความพยายามของพวกเขา การได้ฟังคุณพูดกับเพื่อนและครอบครัวเกี่ยวกับพฤติกรรมที่ดีของพวกเขาจะช่วยส่งเสริมให้พวกเขามีพฤติกรรมที่ดีต่อไป คุณอาจจะพบว่าพวกเขาอาจจะถามว่า “แม่ช่วยบอกคุณยายได้ไหมว่าวันนี้ผมเป็นเด็กดี” ก็ได้
</p>
